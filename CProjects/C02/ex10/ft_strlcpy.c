unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	src_len;

	src_len = 0;
	while (src[src_len])
		src_len++;
	if (size == 0)
		return (src_len);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (src_len);
}

#include <stdio.h>

int	main(void)
{
	char	dest[3] = "oi";
	char	src[6] = "mundo";

	ft_strlcpy(dest, src, 5);
	printf("%s", dest);

	return (0);
}
